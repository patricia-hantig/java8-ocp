package com.patri.java.ocp._1_advanced_class_design._6_nested_classes;

public class Definition {
    // ■ Nested class = class that is defined within another class
    // ■ Inner classes = all nested classes that are not static

    // ■ Types of nested classes:
    // - Inner classes:
    //          - Inner class (member inner class) = is defined at the same level as instance variables
    //          - Local inner class = is defined within a method
    //          - Anonymous inner class = is a special local inner class that doesn't have a name
    // - Static nested class = is defined at the same level as static variables

    // ■ Usage of inner classes:
    // - helper classes - restricting them to the containing class
}
