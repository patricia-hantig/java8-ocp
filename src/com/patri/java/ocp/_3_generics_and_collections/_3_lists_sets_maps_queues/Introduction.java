package com.patri.java.ocp._3_generics_and_collections._3_lists_sets_maps_queues;

import java.util.Collections;

public class Introduction {
    public static void main(String[] args) {

        // Collection = group of objects contained in a single object
        // Java Collections Framework = set of classes in java.util for storing collections

        // Java Collections Framework has 4 main interfaces:
        // ■ List = is an ordered collections of elements that allows duplicate entries; the elements can be accessed by an int index
        // ■ Set = is a collection that doesn't allow duplicates
        // ■ Queue = is a collection that orders its elements in a specific order for processing (usually FIFO)
        // ■ Map = is a collection that maps keys to values with no duplicate keys; elements in a map are key/value pairs

        // Collection interface is superinterface for List Interface, Set Interface and Queue Interface
        // Map isn't technically a Collection, it is a collection - maps are treated differently because they are key/value pairs
    }
}
